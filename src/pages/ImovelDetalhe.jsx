export function ImovelDetalhe() {
  const { codigo } = useParams();
  const [loading, setLoading] = useState(true);
  const [imovel, setImovel] = useState(null);

  useEffect(() => {
    const timer = setTimeout(() => {
      const encontrado = imoveisMock.find(
        (i) => i.codigo.toLowerCase() === codigo.toLowerCase()
      );
      setImovel(encontrado);
      setLoading(false);
    }, 1000);

    return () => clearTimeout(timer);
  }, [codigo]);

  useEffect(() => {
    if (imovel) {
      document.title = `${imovel.imovel} - MC Imobiliária`;
    } else {
      document.title = 'Imóvel não encontrado - MC Imobiliária';
    }
  }, [imovel]);

  if (loading) return <Loader />;

  return (
    <>
      <Header />
      <Container>
        {imovel ? (
          <>
            <ImagemContainer>
              <Carousel showThumbs={true} infiniteLoop useKeyboardArrows dynamicHeight>
                {imovel.imagens?.map((src, index) => (
                  <div key={index}>
                    <img src={src} alt={`${imovel.imovel} imagem ${index + 1}`} />
                  </div>
                ))}
              </Carousel>
            </ImagemContainer>

            <Info>
              <h1>{imovel.imovel} ({imovel.codigo})</h1>
              <InfoRow>
                <span><strong>Tipo:</strong> {imovel.tipo}</span>
                <span><strong>Cidade:</strong> {imovel.cidade}</span>
                <span><strong>Bairro:</strong> {imovel.bairro}</span>
              </InfoRow>
              <PriceHighlight>{imovel.preco}</PriceHighlight>
              <CTAButtons>
                <button className="primary">Agendar Visita</button>
                <button className="secondary">Compartilhar</button>
              </CTAButtons>
            </Info>

            <Detalhes>
              <h2>Descrição</h2>
              <p>{imovel.descricao}</p>
            </Detalhes>

            <Facilidades>
              <h2>Facilidades</h2>
              <ul>
                <li>Estacionamento</li>
                <li>Boa Circulação</li>
                <li>Área para carga e descarga</li>
                <li>Pé direito alto</li>
              </ul>
            </Facilidades>
          </>
        ) : (
          <p style={{ color: 'red' }}>
            Imóvel com código <strong>{codigo}</strong> não encontrado.
          </p>
        )}
      </Container>
      <Footer />
    </>
  );
}
